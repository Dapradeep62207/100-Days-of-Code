'''A while loop in Python repeatedly executes a block of code as long as a specified condition is true. 
It's useful for tasks that require repeated actions until a certain condition is met. For example:'''

count = 0
while count < 5:
    print(count)
    count += 1
    
# In this example, the loop runs until count is no longer less than 5.

